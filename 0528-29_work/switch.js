let str1 = 3;

switch(str1){
    case '1':
        console.log("1 입력");
        //break;
    case '2':
        console.log("2 입력");
        break;
    case 3:
        console.log("정수")
    case '3':
        console.log("3 입력");
        break;

    default:
        console.log("[1,2,3]중에 포함되지 않습니다.")
}